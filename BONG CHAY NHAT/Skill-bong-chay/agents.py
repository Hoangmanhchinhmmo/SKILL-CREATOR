# Agent 1: データ収集 (Data Collector)
# ============================================================

AGENT1_SYSTEM = """あなたはNPBデータリサーチャーです。

ユーザーの入力から、以下の情報を整理して出力してください：

1. 対象チーム名（ひらがな表記）
2. 対戦カード（あれば）
3. 現在の順位（セ・リーグ / パ・リーグ）
4. 直近5〜10試合の勝敗と調子
5. 注目の先発投手（予想）
6. 主力打者の最近の調子
7. 救援陣の疲労状況
8. けが人・欠場者の情報
9. 対戦成績（シーズン通算）
10. 球場・天候・スケジュール要因

【ルール】
- 出力はすべて日本語
- 英語は一切使用禁止
- 日本人名はひらがなで書く
- 外国人名はカタカナで書く
- 数字はアラビア数字
- 情報が不明な場合は「不明」と書き、推測で補わない
- 箇条書きで簡潔に
"""


def agent_data_collector(user_input: str) -> str:
    """Agent 1: NPBデータ収集 (NotebookLM + Gemini)

    1. NotebookLMから実データを取得
    2. Geminiで整理・補完
    """
    # Step 1: NotebookLMからデータ収集
    nlm_data = ""
    try:
        from nlm_data import collect_npb_data
        nlm_data = collect_npb_data(user_input)
    except Exception:
        pass  # NotebookLM使えない場合はGeminiのみ

    # Step 2: Geminiで整理
    if nlm_data:
        prompt = f"""以下はNotebookLMから取得した実データです。
このデータをもとに、試合プレビューに必要な情報を整理してください。
データに不足がある場合は、一般知識で補完してください。

【ユーザーの入力】
{user_input}

【NotebookLMデータ】
{nlm_data}"""
    else:
        prompt = user_input

    return _call_gemini(AGENT1_SYSTEM, prompt, "data_collector")


